//number ada 2 yaitu int dan double
// int untuk bilangan bulat, untuk bilangan desimal 0.5 gunakan double

void main() {
  int number1 = 10; // tidak bisa dirubah nilai nya menjadi desimal
  double number2 = 10.5; // tidak bisa dirubah nilai nya ke int

  print(number1);
  print(number2);

// tipe data yg bisa menggunakan int dan double adalah 'num'
  num number = 10;
  print(number);

  number = 10.5;
  print(number);
}
