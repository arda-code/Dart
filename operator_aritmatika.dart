void main() {
  var first = 10;
  var second = 3;

  print(first + second); //tambah
  print(first - second); //kurang
  print(first * second); //kali
  print(first / second); //hasil bagi
  print(first ~/ second); //hasil bagi berupa int atau digenapkan
  print(first % second); // sisa pembagian
}
